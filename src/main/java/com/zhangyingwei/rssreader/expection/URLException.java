package com.zhangyingwei.rssreader.expection;

public class URLException extends Exception {

	public URLException() {
		super();
	}

	public URLException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public URLException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public URLException(String arg0) {
		super(arg0);
	}

	public URLException(Throwable arg0) {
		super(arg0);
	}
	
}
